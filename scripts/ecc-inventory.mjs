#!/usr/bin/env node
// ECC canonical inventory (Task P5.1, resolution F1).
//
// Scans ONLY the canonical ECC skills dir — the ~277 unique skill directories,
// each holding a SKILL.md. It deliberately does NOT recurse into harness mirror
// copies (.claude/, .cursor/, docs/, etc.), so a skill is counted exactly once.
//
// Pure core: `inventory(eccSkillsDir)` — unit-tested with an injectable dir.
// CLI wrapper: `node scripts/ecc-inventory.mjs [eccSkillsDir]` — writes
//   docs/plans/ecc-merge-ledger-full.md (one row per unique skill).

import { readdirSync, statSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));

// Canonical ECC skills location (read-only clone).
export const DEFAULT_ECC_SKILLS_DIR = '/development/_sources/ECC/skills';

/**
 * Parse the leading YAML frontmatter block of a SKILL.md.
 * Minimal, dependency-free: handles the flat `key: value` scalars we need
 * (name, description, concern). Nested keys (e.g. `metadata:`) are skipped.
 * @param {string} text
 * @returns {Record<string,string>}
 */
export function parseFrontmatter(text) {
  const out = {};
  if (!text.startsWith('---')) return out;
  const end = text.indexOf('\n---', 3);
  if (end === -1) return out;
  const block = text.slice(text.indexOf('\n') + 1, end);
  for (const rawLine of block.split('\n')) {
    // Only top-level keys (no leading whitespace → not a nested mapping entry).
    if (/^\s/.test(rawLine)) continue;
    const m = rawLine.match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!m) continue;
    let value = m[2].trim();
    // Strip matching surrounding quotes.
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    if (value === '') continue; // e.g. `metadata:` — a nested-block header.
    out[m[1]] = value;
  }
  return out;
}

/**
 * Build the canonical inventory of ECC skills.
 *
 * Returns one entry per DIRECT subdirectory of `eccSkillsDir` that contains a
 * `SKILL.md`. It does not recurse, so harness mirror copies nested under a
 * skill (e.g. `<skill>/.claude/skills/.../SKILL.md`) are never double-counted.
 *
 * @param {string} [eccSkillsDir] canonical ECC/skills dir (injectable for tests)
 * @returns {Array<{name:string, description?:string, concern?:string, dir:string}>}
 */
export function inventory(eccSkillsDir = DEFAULT_ECC_SKILLS_DIR) {
  const root = resolve(eccSkillsDir);
  const entries = [];

  for (const name of readdirSync(root)) {
    const dir = join(root, name);
    let st;
    try {
      st = statSync(dir);
    } catch {
      continue;
    }
    if (!st.isDirectory()) continue;

    const skillFile = join(dir, 'SKILL.md');
    if (!existsSync(skillFile)) continue; // direct SKILL.md required

    const fm = parseFrontmatter(readFileSync(skillFile, 'utf8'));
    entries.push({
      name: fm.name || name, // fall back to the directory name
      description: fm.description,
      concern: fm.concern,
      dir,
    });
  }

  entries.sort((a, b) => a.name.localeCompare(b.name));
  return entries;
}

/**
 * Render the merge ledger as a markdown table.
 * Columns: skill | concern | disposition. Disposition starts as TODO
 * (P5.2 fills these in).
 * @param {ReturnType<typeof inventory>} entries
 * @returns {string}
 */
export function renderLedger(entries) {
  const lines = [];
  lines.push('# ECC Merge Ledger (Full)');
  lines.push('');
  lines.push(
    `Canonical inventory of the ${entries.length} unique ECC skills ` +
      '(one row per skill directory bearing a `SKILL.md`).',
  );
  lines.push('');
  lines.push('Generated by `scripts/ecc-inventory.mjs`. Dispositions are `TODO`');
  lines.push('until Task P5.2 resolves each skill.');
  lines.push('');
  lines.push('| skill | concern | disposition |');
  lines.push('| --- | --- | --- |');
  for (const e of entries) {
    const concern = (e.concern || '').replace(/\|/g, '\\|');
    lines.push(`| ${e.name} | ${concern} | TODO |`);
  }
  lines.push('');
  return lines.join('\n');
}

// --- CLI ---------------------------------------------------------------------

const isMain =
  process.argv[1] && resolve(process.argv[1]) === fileURLToPath(import.meta.url);

if (isMain) {
  const eccSkillsDir = process.argv[2] || DEFAULT_ECC_SKILLS_DIR;
  if (!existsSync(eccSkillsDir) || !statSync(eccSkillsDir).isDirectory()) {
    console.error(`Not a directory: ${eccSkillsDir}`);
    process.exit(1);
  }
  const entries = inventory(eccSkillsDir);
  const outPath = resolve(__dirname, '..', 'docs', 'plans', 'ecc-merge-ledger-full.md');
  writeFileSync(outPath, renderLedger(entries));
  console.log(
    `Inventoried ${entries.length} unique ECC skill(s) from ${resolve(eccSkillsDir)}`,
  );
  console.log(`Wrote ledger: ${outPath}`);
}
